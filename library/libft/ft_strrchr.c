#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int j;

	i = 0;
	j = -1;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			j = i;
		i++;
	}
	if (s[i] == (char)c)
		j = i;
	if (j == -1)
		return (NULL);
	return ((char *)&s[j]);
}
